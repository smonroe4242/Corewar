#include "../inc/corewar.h"

uint16_t	acb_len(uint8_t acb)
{
	//TEA
	uint16_t	len;

	len = 2;
	len += (((acb >> 6) == 3) ? 4 : (acb >> 6));
	len += ((((acb >> 4) & 3) == 3) ? 4 : ((acb >> 4) & 3));
	len += ((((acb >> 2) & 3) == 3) ? 4 : ((acb >> 2) & 3));
	//TIME("acb_len\t")
	return (len);
}

#include "../inc/libft.h"

uint64_t	ft_swapendian64(uint64_t i)
{
	i = (i & 0x00000000FFFFFFFF) << 32 | (i & 0xFFFFFFFF00000000) >> 32;
	i = (i & 0x0000FFFF0000FFFF) << 16 | (i & 0xFFFF0000FFFF0000) >> 16;
	i = (i & 0x00FF00FF00FF00FF) << 8 | (i & 0xFF00FF00FF00FF00) >> 8;
	return (i);
}

#include "../inc/corewar.h"

void	pc_free(t_pc *pc)
{
	t_pc	*tmp;

	tmp = pc;
	while (tmp)
	{
		tmp = tmp->next;
		ft_bzero(pc, sizeof(t_pc));
		free(pc);
		pc = tmp;
	}
}
